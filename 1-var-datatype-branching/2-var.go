package main

import "fmt"

func main() {
	//long declaration
	var age uint
	fmt.Println(age)

	var bilangan1 = 100
	fmt.Println(bilangan1)

	var bilangan2 float64 = 89.5
	fmt.Println(bilangan2)

	var firstName string = "Budi"
	fmt.Println(firstName)

	var cek bool = true
	fmt.Println(cek)

	var bil1, bil2 int = 5, 8
	fmt.Println(bil1) // 5
	fmt.Println(bil2) // 8

	var nama = "rudi"
	fmt.Println(nama)

	// short declaration
	country := "Indonesia"
	fmt.Println(country)

	angka1 := 90
	fmt.Println(angka1)

	// constanta
	const pi float64 = 3.14

}
